# involves finding greatest common denominator of two numbers
# GCD(A,B)
# if A = 0, GCD = B
# if B = 0, GCD = A
# A = B * QUOTIENT + REMAINDER or A = B * Q + R
# Example, find GCD of A = 270 and B = 192
# 270 = 192 * 1 + 78
# 192 = 78 * 2 + 36
# 78 = 36 * 2 + 6
# 36 = 6 * 6 + 0
# A = 6, B = 0
# GCD = 6
